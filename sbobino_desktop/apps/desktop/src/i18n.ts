let currentLanguage: AppLanguage = "en";
const listeners: Set<() => void> = new Set();

function notifyListeners() {
  for (const listener of listeners) {
    listener();
  }
}

export function t(key: string, fallback?: string): string {
  return translations[currentLanguage]?.[key] ?? translations.en[key] ?? fallback ?? key;
}

export function changeLanguage(lang: AppLanguage): void {
  if (currentLanguage !== lang) {
    currentLanguage = lang;
    notifyListeners();
  }
}

export function getCurrentLanguage(): AppLanguage {
  return currentLanguage;
}

export function useTranslation(): { t: (key: string, fallback?: string) => string; language: AppLanguage } {
  const [, setTick] = useState(0);

  React.useEffect(() => {
    const listener = () => setTick((n) => n + 1);
    listeners.add(listener);
    return () => { listeners.delete(listener); };
  }, []);

  return { t, language: currentLanguage };
}
