export const translationsCatalog = translations;

let currentLanguage: AppLanguage = "en";
const listeners: Set<() => void> = new Set();

function notifyListeners() {
  for (const listener of listeners) {
    listener();
  }
}

function interpolateTranslation(
  template: string,
  values?: TranslationValues,
): string {
  if (!values) {
    return template;
  }

  return template.replace(/\{(\w+)\}/g, (_match, key: string) => {
    const value = values[key];
    return value === undefined || value === null ? `{${key}}` : String(value);
  });
}

export function t(key: string, fallback?: string, values?: TranslationValues): string {
  const template = translations[currentLanguage]?.[key] ?? translations.en[key] ?? fallback ?? key;
  return interpolateTranslation(template, values);
}

export function changeLanguage(lang: AppLanguage): void {
  if (currentLanguage !== lang) {
    currentLanguage = lang;
    notifyListeners();
  }
}

export function getCurrentLanguage(): AppLanguage {
  return currentLanguage;
}

export function useTranslation(): {
  t: (key: string, fallback?: string, values?: TranslationValues) => string;
  language: AppLanguage;
} {
  const [, setTick] = useState(0);

  React.useEffect(() => {
    const listener = () => setTick((n) => n + 1);
    listeners.add(listener);
    return () => { listeners.delete(listener); };
  }, []);

  return { t, language: currentLanguage };
}
